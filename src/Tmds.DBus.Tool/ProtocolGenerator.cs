            {
                var sortedProp = sortedProperties.First(p => p.Name == prop.Name);
                string writeMethodName = GetWriteTypeMethodName(prop.Signature, variant: true);
                AppendLine($"if ({sortedProp.NameLower} is not null)");
                StartBlock();
